package Day09.Task2;

public class Triangle extends Figure{

    private double a;
    private double b;
    private double c;

    public Triangle(String color, double a, double b, double c) {
        super(color);
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }

    public double getC() {
        return c;
    }

    public void setC(double c) {
        this.c = c;
    }

    @Override
    public double area() {
        double p=perimeter()/2;
        return Math.sqrt(p*(p-a)*(p-a)*(p-c));
    }

    @Override
    public double perimeter() {
        return a+b+c;
    }
}
